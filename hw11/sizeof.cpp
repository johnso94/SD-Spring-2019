#include<iostream>
using namespace std;
int main(){
    cout << "Size of a short is " << sizeof(short) << " bytes" << endl;
    cout << "Size of an int is " << sizeof(int) << " bytes" << endl;
    cout << "Size of a double is " << sizeof(double) << " bytes" << endl;
    cout << "Size of a pointer to a short is " << sizeof(short *) << " bytes" << endl;
    cout << "Size of a pointer to an int is " << sizeof(int *) << " bytes" << endl;
    cout << "Size of a pointer to a double is " << sizeof(double *) << " bytes" << endl;
    
}